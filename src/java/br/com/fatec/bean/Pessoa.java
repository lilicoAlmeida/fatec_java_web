package br.com.fatec.bean;

public class Pessoa {
    //ATRIBUTOS
    protected int id;
    protected String nome;

    //GETTERS E SETTERS
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    } 
}
